package datentypen;

public class PrimDatentypen {

	public static void main(String[] args) {

		int a = 10;
		char b = 11;
		boolean c = false;
		boolean d = true;
		boolean e = !false;
		float f = 3.7f;
		System.out.println(a);

		System.out.println(b);
		System.out.println(c);

		System.out.println(d);
		System.out.println(e);

		System.out.println(f);
	}

}
